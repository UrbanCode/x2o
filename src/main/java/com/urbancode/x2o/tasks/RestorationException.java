package com.urbancode.x2o.tasks;

public class RestorationException extends Exception {

    //**********************************************************************************************
    // CLASS
    //**********************************************************************************************
    static private final long serialVersionUID = 1L;

    //**********************************************************************************************
    // INSTANCE
    //**********************************************************************************************

    //----------------------------------------------------------------------------------------------
    public RestorationException() {
        super();
    }

    //----------------------------------------------------------------------------------------------
    public RestorationException(String message, Throwable cause) {
        super(message, cause);
    }

    //----------------------------------------------------------------------------------------------
    public RestorationException(String message) {
        super(message);
    }

    //----------------------------------------------------------------------------------------------
    public RestorationException(Throwable cause) {
        super(cause);
    }
}
